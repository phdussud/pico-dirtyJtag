import usb1
import time

VID = 0x1209
PID = 0xC0CA
EP_OUT = 0x01
EP_IN  = 0x82

def send_cmd(handle, data):
    handle.bulkWrite(EP_OUT, data)
    try:
        resp = handle.bulkRead(EP_IN, 64, timeout=100)
        print("IN:",resp)
    except usb1.USBErrorTimeout:
        print("No IN response")

def main():
    ctx = usb1.USBContext()
    handle = ctx.openByVendorIDAndProductID(VID, PID,
                                            skip_on_error=True)
    if handle is None:
        print("Device not found")
        return
    with handle.claimInterface(0):
        to_send = bytes([0x6, 0x10, 0x1, 0x6, 0x10, 0x1, 0x6, 0x0, 0x1, 0x6, 0x0, 0x1, 0x83, 0x8, 0xdf, 0x4,
                         0x16, 0x14, 0x4, 0x16, 0x16, 0x4, 0x16, 0x14, 0x6, 0x10, 0x1, 0x6, 0x0, 0x1, 0x6, 0x10,
                         0x1, 0x6, 0x0, 0x1, 0x6, 0x0, 0x1, 0x3, 0x23, 0x80, 0x0, 0x0, 0x0, 0x0, 0x4, 0x16, 0x10,
                         0x4, 0x16, 0x12, 0x5, 0x4, 0x16, 0x10, 0x6, 0x10, 0x1, 0x6, 0x0, 0x1, 0x0])
        cmd_val = 0x03  # CMD_XFER, no modifiers
        send_cmd(handle, to_send)

if __name__ == "__main__":
    main()